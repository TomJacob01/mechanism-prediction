"""Generate ``docs/repo-map.md`` — a per-module symbol index for AI agents.

Walks ``src/mech_uspto/`` and ``scripts/`` with the Python AST, extracts each
file's module docstring (first line), public classes, and public functions
with signatures. Output is plain Markdown so it diffs cleanly and renders in
any viewer.

This file is intentionally side-effect free at import time (no top-level
prints, no analysis on import). Run it as a script.

Usage::

    python scripts/gen_repo_map.py
    python scripts/gen_repo_map.py --out docs/repo-map.md
    python scripts/gen_repo_map.py --check       # exit non-zero if stale
"""

from __future__ import annotations

import argparse
import ast
import sys
from dataclasses import dataclass
from pathlib import Path
from textwrap import shorten

REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_OUT = REPO_ROOT / "docs" / "repo-map.md"

ROOTS: list[tuple[str, Path]] = [
    ("Library", REPO_ROOT / "src" / "mech_uspto"),
    ("Scripts", REPO_ROOT / "scripts"),
    ("Tests", REPO_ROOT / "tests"),
]


@dataclass
class SymbolRow:
    kind: str       # "class" | "func"
    name: str
    signature: str  # one-line, no body
    docline: str    # first non-empty line of docstring, truncated


@dataclass
class ModuleEntry:
    rel_path: str
    docline: str
    symbols: list[SymbolRow]


def _first_doc_line(node: ast.AST) -> str:
    doc = ast.get_docstring(node) or ""
    for line in doc.splitlines():
        line = line.strip()
        if line:
            return shorten(line, width=140, placeholder="…")
    return ""


def _is_public(name: str) -> bool:
    return not name.startswith("_")


def _format_args(args: ast.arguments) -> str:
    parts: list[str] = []
    posonly = [a.arg for a in args.posonlyargs]
    if posonly:
        parts.extend(posonly)
        parts.append("/")
    parts.extend(a.arg for a in args.args)
    if args.vararg is not None:
        parts.append("*" + args.vararg.arg)
    elif args.kwonlyargs:
        parts.append("*")
    parts.extend(a.arg for a in args.kwonlyargs)
    if args.kwarg is not None:
        parts.append("**" + args.kwarg.arg)
    return "(" + ", ".join(parts) + ")"


def _walk_module(path: Path) -> ModuleEntry | None:
    try:
        tree = ast.parse(path.read_text(encoding="utf-8"))
    except (SyntaxError, UnicodeDecodeError):
        return None

    rel = path.relative_to(REPO_ROOT).as_posix()
    docline = _first_doc_line(tree)
    symbols: list[SymbolRow] = []
    for node in tree.body:
        if isinstance(node, ast.ClassDef) and _is_public(node.name):
            symbols.append(SymbolRow(
                kind="class",
                name=node.name,
                signature=f"class {node.name}",
                docline=_first_doc_line(node),
            ))
        elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and _is_public(node.name):
            symbols.append(SymbolRow(
                kind="func",
                name=node.name,
                signature=f"{node.name}{_format_args(node.args)}",
                docline=_first_doc_line(node),
            ))
    return ModuleEntry(rel_path=rel, docline=docline, symbols=symbols)


def _collect(root: Path) -> list[ModuleEntry]:
    out: list[ModuleEntry] = []
    if not root.exists():
        return out
    for py in sorted(root.rglob("*.py")):
        # Skip caches, build outputs, dunder-init noise (handled but kept).
        if "__pycache__" in py.parts or ".egg-info" in str(py):
            continue
        entry = _walk_module(py)
        if entry is not None:
            out.append(entry)
    return out


def _render(sections: list[tuple[str, list[ModuleEntry]]]) -> str:
    lines: list[str] = [
        "# Repo map",
        "",
        "Auto-generated by `python scripts/gen_repo_map.py`. **Do not edit by hand** — "
        "your changes will be overwritten. Regenerate after adding or removing modules.",
        "",
        "Each entry shows: module path, module docstring (first line), and public "
        "classes/functions with one-line summaries.",
        "",
    ]
    for header, modules in sections:
        lines.append(f"## {header}")
        lines.append("")
        if not modules:
            lines.append("_(no modules)_")
            lines.append("")
            continue
        for m in modules:
            lines.append(f"### [{m.rel_path}]({_to_link(m.rel_path)})")
            if m.docline:
                lines.append("")
                lines.append(f"_{m.docline}_")
            if m.symbols:
                lines.append("")
                lines.append("| Kind | Symbol | Summary |")
                lines.append("|---|---|---|")
                for s in m.symbols:
                    sig = s.signature.replace("|", "\\|")
                    doc = s.docline.replace("|", "\\|")
                    lines.append(f"| {s.kind} | `{sig}` | {doc} |")
            lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def _to_link(rel_path: str) -> str:
    # docs/repo-map.md lives one level inside docs/, so link relative to it.
    return "../" + rel_path


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", type=Path, default=DEFAULT_OUT)
    ap.add_argument("--check", action="store_true",
                    help="Exit 1 if the generated content differs from --out.")
    args = ap.parse_args()

    sections = [(name, _collect(root)) for name, root in ROOTS]
    content = _render(sections)

    if args.check:
        if not args.out.exists():
            print(f"[gen_repo_map] {args.out} does not exist; run without --check to create it.")
            sys.exit(1)
        existing = args.out.read_text(encoding="utf-8")
        if existing != content:
            print(f"[gen_repo_map] {args.out} is stale. Run: python scripts/gen_repo_map.py")
            sys.exit(1)
        print(f"[gen_repo_map] {args.out} is up to date.")
        return

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(content, encoding="utf-8")
    n_modules = sum(len(ms) for _, ms in sections)
    n_symbols = sum(len(m.symbols) for _, ms in sections for m in ms)
    print(f"[gen_repo_map] wrote {args.out}  ({n_modules} modules, {n_symbols} public symbols)")


if __name__ == "__main__":
    main()
